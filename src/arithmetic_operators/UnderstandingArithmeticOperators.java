package arithmetic_operators;

public class UnderstandingArithmeticOperators {
    public static void main(String[] args) {

        int num1 = 8;
        int num2 = 2;

        System.out.println(num1 + num2);
        System.out.println(num1 - num2);
        System.out.println(num1 / num2);
        System.out.println(num1 * num2);
        System.out.println(num1 % num2);
    }
}
