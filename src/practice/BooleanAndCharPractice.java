package practice;

public class BooleanAndCharPractice {
    public static void main(String[] args){
        boolean negative = false;
        boolean positive = true;

        System.out.println("negative is = " + negative);
        System.out.println("positive is = " + positive);

        char myFirstChar = 'H';
        char mySecondChar = 'A';
        char myThirdChar = 'D';
        char myFourthChar = 'I';
        System.out.println("");

        System.out.print(myFirstChar);
        System.out.print(mySecondChar);
        System.out.print(myThirdChar);
        System.out.print(myFourthChar);

        System.out.println("");

        char myFirstLetter = 'D';
        char mySecondLetter = 'O';
        char myThirdLetter = 'G';

        System.out.print("\"" + myFirstLetter);
        System.out.print(mySecondLetter);
        System.out.print(myThirdLetter + "\"");



    }
}
